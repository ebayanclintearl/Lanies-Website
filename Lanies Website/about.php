        <h1>Our Story</h1>
        <p>Lanie of Lanie's Batac Special Empanada is the grand daughter of Lola Nieves"Ibing" Cocson Layaoen the undoubted "Mother of Batac Empanada".
            Lola Ibing is credited as the one who pioneered the Empanada trade and the originator of the Batac version of Spanish Empanada using locally produced crops such as mongo boiled perfectly to the right al dente, grated fresh papaya sauteed altogether in fish sauce along with the essence rich Ilocos Garlic and other spices then enwrapped in a tasty, crusty and crunchy taco like wrapper derived from rice flour subsequently deep fried to the right oil temperature.
            It's intriguing and tempting orange pigment is a result of the organic food color achuete. 
            During the course of time the plain Batac Empanada evolved by the addition of egg, longganisa, hotdog or combination of any, according to customer's desire. Like her Lola Ibing, Lanie is again introducing a new twists to the Batac Empanada by adding the famous Batac Bagnet ("Sisaron"), a Local product Batac also known for. She also added a nutritios variants the Malunngay Empanada and Cheesy Empanada. Pushing further Lanie together with her friend Joemar Ordania made the Batac version of the mexican tacos/nachos using the Batac empanada ingredients, the now becoming new food fad the captivating TACOS Empanada.</p>
